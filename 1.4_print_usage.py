# 1.4.print语句的用法

# 1.4.1 用print语句“画”一只狗
print('o----')
print(' ||||')
'''
运行结果：
----------------
o----
 ||||
----------------
'''

# 1.4.2 字符串与乘法运算符结合——重复输出指定字符串
print('*' * 10)
'''
运行结果：
-----------------
**********
-----------------
'''

# 1.4.3小练习——使用print语句画一个形状
print('''*
**
***
****
***** 
''')

# 1.4.4 print括号中用加号”+“连接多个字符串（之后会讲到）
name = 'Mosh'
print('Hi, ' + name + '!')
'''
运行结果：
------------
Hi, Mosh!
------------
'''
