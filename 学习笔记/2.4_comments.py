# 2.4 注释
# 2.4.1 单行注释 （这一行其实就是一条单行注释）
print('Sky is blue')
'''
运行结果：
-----------------
Sky is blue
-----------------
结论：print上一行的语句不会被执行(或打印)，因为跟在“#”后面
'''
