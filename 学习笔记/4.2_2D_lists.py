# 4.2.3 使用嵌套循环来遍历矩阵中的所有元素
# 本部分知识涉及到5.4嵌套循环的知识，可以在学习完第五章相关内容（5.3或5.4）后再回看
matrix = [             # 外部列表
    [1, 2, 3],
    [4, 5, 6],         # 内部列表（每行都是一个列表）
    [7, 8, 9]
]
for row in matrix:     # 外循环：每一次迭代的row变量用于存储一行的列表
    for item in row:   # 内循环：遍历当前一行的所有元素
        print(item)
'''
运行结果：
---------
1
2
3
4
5
6
7
8
9
---------
'''
